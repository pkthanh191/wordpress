	<h1>What to do next</h1>

	<p>If the import results look good and error free then there may still be some tasks to perform before going live with your Simple:Press forum:</p>

	<h3>Forum Administrators</h3>

	<p>As the user making the import, you have automatically been made an SP Administrator. If you wish other members to also act as forum Administrators
	then you will need to visit `<b>Admins > Manage Admins</b> to assign them this role.</p>

	<h3>Forum Moderators</h3>

	<p>If you wish to assign specific users as forum Moderators, you will need to visit <b>User Groups > Manage User Groups</b> to do so. When SP was
	installed a default 'Moderators' user group was defined which can optionally be used.</p>

	<h3>Forum Groups</h3>

	<p>Depending upon the source system, it is not always possible to import and re-create your forum Groups - sometimes also known as Categories. If this
	is the case then a single default Group would have been created. Go to <b>Forums > Manage Groups and Forums</b> to create your Groups and to move your
	forums into them.</p>

	<h3>Sub or Child Forums</h3>

	<p>It is not always possible to recreate sub or child forums and if this is the case, they will have been imported as standard forums. Changing these
	to sub forums is very easy and quick and can be accomplished at <b>Forums > Manage Groups and Forums</b></p>

	<h3>Member and Guest Permissions</h3>

	<p>Simple:Press has a comprehensive permission system underpinning what your members and guests can see and do within your forum. Go to
	<b>Permissions > Manage Permissions</b> to set these up as you require them.</p>

	<h3>Options and Components</h3>

	<p>Please spend a little time working through and reviewing the various SP forum options, components and other attributes. Many conditions of your
	source forum can be recreated here and it is less frustrating to ensure you have them set how you want them from the very start of going live.
	Note that each Admin panel comes complete with popup help to explain the various options and settings available.</p>
